    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name=jTextField1.getText();
        String nid=jTextField2.getText();
        String phone=jTextField3.getText();
        String address=jTextField4.getText();
        String email=jTextField5.getText();
        String residentID=jTextField6.getText();
        String occupation=jTextField7.getText();
        String emergencyContact=jTextField8.getText();
        String roomnumber=jComboBox1.getSelectedItem().toString();
        String status="Living";
        
        try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("insert into resident(Name,NID,PHONE,PermanentAddress,EmailId,ResidentId,Occupation,emergencyContact,RoomNumber,residentStatus) values (?,?,?,?,?,?,?,?,?,?)");
            ps.setString(1, name);
            ps.setString(2, nid);
            ps.setString(3, phone);
            ps.setString(4, address);
            ps.setString(5, email);
            ps.setString(6, residentID);
            ps.setString(7, occupation);
            ps.setString(8, emergencyContact);
            ps.setString(9, roomnumber);
            ps.setString(10, status);
            ps.executeUpdate();
            
            PreparedStatement ps1=con.prepareStatement("update rooms set RoomStatus='Booked' where RoomNumber=?");
            ps1.setString(1, roomnumber);
            ps1.executeUpdate();
            JOptionPane.showMessageDialog(null, "successfully updated");
            clear();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

