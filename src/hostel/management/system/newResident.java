   public void roomNumber()
   {
       int i=0;
       try
       {
           Connection con=ConnectionProvider.getCon();
           Statement st=con.createStatement();
           ResultSet rs=st.executeQuery("select* from rooms where activate='YES'and RoomStatus='Not Booked'" );
           while(rs.next())
           {
               i=1;
               jComboBox1.addItem(rs.getString(1));
           }
           if(i==0)
           {
               jButton2.setVisible(false);
               JOptionPane.showMessageDialog(null,"SORRY,ALL ROOMS ARE ALREADY BOOKED");
               setVisible(false);
           }
       }
       catch(Exception e)
       {
           
       }
   }
   
   
